    private void mobileKeyTyped(java.awt.event.KeyEvent evt) {                                
       char c = evt.getKeyChar();
         if((Character.isDigit(c))|| c=='\b')
         {
             mobile.setEditable(true);
         }
         else
         {  JOptionPane.showMessageDialog(null,"Enter only integer");
             mobile.setText(null);
         
         }
    }                               

